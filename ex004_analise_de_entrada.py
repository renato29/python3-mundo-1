a=input('Digite algo moço: ')
print('O tipo primitivo destes valor é:',type(a))
print('Só tem espaço ? ',a.isspace())
print('isto é um numero? ',a.isnumeric())
print('é alfabetico o que foi digitado?', a.isalpha())
print('tem letras maiusculas?', a.isupper())
print('tem letras minusculas?',a.islower())
print('esta capitalizado?',a.istitle())



